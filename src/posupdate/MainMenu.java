    public void tabel(){
        DefaultTableModel model1 = new DefaultTableModel();

        model1.addColumn("Kode Barang");
        model1.addColumn("Nama Barang");
        model1.addColumn("Harga");
        model1.addColumn("Jumlah");
        model1.addColumn("Nama Supplier");
        model1.addColumn("Kode Pembelian");
        model1.addColumn("Nama Pegawai");
        model1.addColumn("Tanggal");
        
        try {
            Connection conn = projectpos.Config.configDB();
            String sql = "SELECT barang.kode_barang, barang.nama_barang, barang.harga, barang.jumlah, "
                    + "supplier.nama_supplier,"
                    + "pembelian.kode_pembelian, pembelian.kode_pegawai, pembelian.tanggal "
                    + "FROM barang "
                    + "JOIN detail_pembelian ON barang.kode_barang = detail_pembelian.kode_barang "
                    + "JOIN pembelian ON detail_pembelian.kode_pembelian = pembelian.kode_pembelian "
                    + "JOIN supplier ON pembelian.kode_supplier = supplier.kode_supplier "
                    + "WHERE barang.nama_barang "
                    + "LIKE '%"+txt_search.getText()+"%'"
                    + "Order By kode_barang";
            java.sql.PreparedStatement ps = conn.prepareStatement(sql);
            java.sql.ResultSet rs = ps.executeQuery(sql);
            while(rs.next()){
                model1.addRow(new Object[]{
                    rs.getString(1), rs.getString(2), rs.getString(3),
                    rs.getString(4), rs.getString(5), rs.getString(6), 
                    rs.getString(7), rs.getString(8),
                });
                
            }
            
  
        } catch (SQLException e) {
            System.out.println(e);
        }
        
        tabel.setModel(model1);
    }
    
